import serial
import time
from myData import all_data

########################################################################################################################



########################################################################################################################



########################################################################################################################




#########################################################################################################################



# 打开串口
ser = serial.Serial('COM5', 250000)
time.sleep(2)  # 给串口一些时间来初始化



# 发送信息
for hex_datas in all_data:
    for hex_data in hex_datas:
        byte_data = bytes.fromhex(hex_data)
        ser.write(byte_data)
        # time.sleep(0.01)

# 关闭串口
ser.close()
